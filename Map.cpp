void Map::loadMap(map_type map)
{
    for (index_type row{ 0 }; row < map.size(); ++row)
    {
        for (index_type column{ 0 }; column < map[0].size(); ++column)
        {
            m_map[row][column] = map[row][column];
        }
    }
}

